from typing import Dict, Any, Awaitable
from typing_extensions import Protocol
from rl.experiments.bandit.controller_protocol import KArmedControllerViewerHandle

class KArmedViewer(Protocol):
    def set_controller(self, controller: KArmedControllerViewerHandle): ...
    def open(self): ...
    def close(self): ...
    async def update(
        self, state: Dict[str, Any]) -> Awaitable[None]: ...

